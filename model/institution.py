class Institution:
    def __init__(self):
        self.instId = ""
        self.instName = ""
        self.instAbbrv = ""
        self.instLocation = ""

    def set_instId(self, instId):
        self.instId = instId

    def set_instName(self,instName):
        self.instName = instName

    def set_instAbbrv(self, instAbbrv):
        self.instAbbrv = instAbbrv

    def set_instLocation(self, instLocation):
        self.instLocation = instLocation